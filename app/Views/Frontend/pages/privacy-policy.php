<li>Read contacts: [Optional] to easily add Emergency Contacts. You can deny this permission and add your Emergency Contact manually.</li>
<li>Send SMS: Required to send SMS to your Emergency Contacts during emergencies.&nbsp;</li>
<li>Make and manage phone calls: We do not make any phone calls, but this permission is somehow required by Android to send SMS. The Send SMS permission is not enough.</li>
<li>Microphone: [Not mandatory but suggested] To send audio recordings either to your Emergency Contacts or to request audios of your device from the website&nbsp;<a href="https://hammer-security.ca/signin">https://w</a>eb.privatech.in</li>
<li>Storage: To store audios / pictures / videos and send them to your Emergency Contacts or to your email.</li>
<li>Battery no restrictions: To let your system know that Privatech should always be running on the background.</li>
<li>Auto start (for some devices): This permission is only required for a few devices. It lets your system know that Privatech can auto start at any time. This allows Privatech to work properly.</li>
